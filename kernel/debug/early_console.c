/**
 * adapted from:
 * https://github.com/Takao-OS/Takao/blob/master/source/services/terminal/
 *
 * we disable ubsan for these functions mainly for performance reasons, because this
 * is going to run alot
 */

// framebuffer state
static uint32_t* g_framebuffer = NULL;
static size_t g_width, g_height, g_pitch;

// console state
static size_t g_rows, g_cols;
static size_t g_current_row = 0, g_current_col = 0;

// font stuff
#define FONT_WIDTH 8
#define FONT_HEIGHT 16
uint8_t* get_font_char(char c);

static char* g_grid = NULL;

void init_early_console(directptr_t framebuffer, size_t width, size_t height, size_t pitch) {
    // framebuffer stuff
    g_framebuffer = framebuffer;
    g_width = width;
    g_height = height;
    g_pitch = pitch / 4;

    // terminal stuff
    g_rows = height / FONT_HEIGHT;
    g_cols = width / FONT_WIDTH;
    g_grid = pmalloc(g_rows * g_cols);
    memset(g_grid, ' ', g_rows * g_cols);

    // mape as wc
    for (directptr_t addr = ALIGN_DOWN(framebuffer, PAGE_SIZE); addr < ALIGN_UP(framebuffer + height * pitch, PAGE_SIZE); addr += PAGE_SIZE) {
        vmm_cache(&g_kernel.address_space, addr, CACHE_WRITE_COMBINING);
    }
    memset(framebuffer, 0x2c, height * pitch);
}

__attribute__((no_sanitize("undefined")))
static void put_pixel(size_t x, size_t y, uint32_t color) {
    g_framebuffer[x + g_pitch * y] = color;
}

__attribute__((no_sanitize("undefined")))
static void print_char(size_t row, size_t col, char c) {
    g_grid[col + row * g_cols] = c;

    size_t fb_index_x = col * FONT_WIDTH;
    size_t fb_index_y = row * FONT_HEIGHT;

    uint8_t* font = get_font_char(c);
    for (int y = 0; y < FONT_HEIGHT; y++) {
        int bf = font[y];
        for (int x = 0; x < FONT_WIDTH; x++) {
            if (bf & (1 << (FONT_WIDTH - x - 1))) {
                put_pixel(x + fb_index_x, y + fb_index_y, 0xdcdcdc);
            } else {
                put_pixel(x + fb_index_x, y + fb_index_y, 0x2c2c2c);
            }
        }
    }
}

__attribute__((no_sanitize("undefined")))
static void scroll() {
    for (int row = 1; row < g_rows; row++) {
        for (int col = 0; col < g_cols; col++) {
            print_char(row - 1, col, g_grid[col + row * g_cols]);
        }
    }

    for (int col = 0; col < g_cols; col++) {
        print_char(g_rows - 1, col, ' ');
    }
}

__attribute__((no_sanitize("undefined")))
void early_console_putc(char c) {
    if (g_framebuffer == NULL) {
        return;
    }

    switch (c) {
        case '\n': {
            if (++g_current_row >= g_rows) {
                scroll();
                g_current_row = g_rows - 1;
            }
            g_current_col = 0;
        } break;

        case '\t': {
            early_console_putc(' ');
            early_console_putc(' ');
            early_console_putc(' ');
            early_console_putc(' ');
        } break;

        default: {
            if (g_current_col >= g_cols) {
                g_current_row++;
                g_current_col = 0;
            }
            if (g_current_row >= g_rows) {
                scroll();
            }

            print_char(g_current_row, g_current_col, c);

            g_current_col++;
        } break;
    }
}

